<?php

namespace CarBundle\Entity;

/**
 * Rent
 */
class Rent
{
    /**
     * @var int
     */
    private $id;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }
    /**
     * @var integer
     */
    private $carId;

    /**
     * @var integer
     */
    private $begingTime;

    /**
     * @var integer
     */
    private $endTime;


    /**
     * Set carId
     *
     * @param integer $carId
     *
     * @return Rent
     */
    public function setCarId($carId)
    {
        $this->carId = $carId;

        return $this;
    }

    /**
     * Get carId
     *
     * @return integer
     */
    public function getCarId()
    {
        return $this->carId;
    }

    /**
     * Set begingTime
     *
     * @param integer $begingTime
     *
     * @return Rent
     */
    public function setBegingTime($begingTime)
    {
        $this->begingTime = $begingTime;

        return $this;
    }

    /**
     * Get begingTime
     *
     * @return integer
     */
    public function getBegingTime()
    {
        return $this->begingTime;
    }

    /**
     * Set endTime
     *
     * @param integer $endTime
     *
     * @return Rent
     */
    public function setEndTime($endTime)
    {
        $this->endTime = $endTime;

        return $this;
    }

    /**
     * Get endTime
     *
     * @return integer
     */
    public function getEndTime()
    {
        return $this->endTime;
    }
    /**
     * @var integer
     */
    private $price;


    /**
     * Set price
     *
     * @param integer $price
     *
     * @return Rent
     */
    public function setPrice($price)
    {
        $this->price = $price;

        return $this;
    }

    /**
     * Get price
     *
     * @return integer
     */
    public function getPrice()
    {
        return $this->price;
    }
    /**
     * @var integer
     */
    private $profit;


    /**
     * Set profit
     *
     * @param integer $profit
     *
     * @return Rent
     */
    public function setProfit($profit)
    {
        $this->profit = $profit;

        return $this;
    }

    /**
     * Get profit
     *
     * @return integer
     */
    public function getProfit()
    {
        return $this->profit;
    }
}
