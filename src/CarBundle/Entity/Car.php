<?php

namespace CarBundle\Entity;

/**
 * Car
 */
class Car
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $title;

    /**
     * @var float
     */
    private $price;

    /**
     * @var string
     */
    private $type;

    /**
     * @var string
     */
    private $fuel;

    /**
     * @var bool
     */
    private $active;

    /**
     * @var string
     */
    private $loadCapacity;

    /**
     * @var int
     */
    private $numberOfSeats;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set title
     *
     * @param string $title
     *
     * @return Car
     */
    public function setTitle($title)
    {
        $this->title = $title;

        return $this;
    }

    /**
     * Get title
     *
     * @return string
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * Set price
     *
     * @param float $price
     *
     * @return Car
     */
    public function setPrice($price)
    {
        $this->price = $price;

        return $this;
    }

    /**
     * Get price
     *
     * @return float
     */
    public function getPrice()
    {
        return $this->price;
    }

    /**
     * Set type
     *
     * @param string $type
     *
     * @return Car
     */
    public function setType($type)
    {
        $this->type = $type;

        return $this;
    }

    /**
     * Get type
     *
     * @return string
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * Set fuel
     *
     * @param string $fuel
     *
     * @return Car
     */
    public function setFuel($fuel)
    {
        $this->fuel = $fuel;

        return $this;
    }

    /**
     * Get fuel
     *
     * @return string
     */
    public function getFuel()
    {
        return $this->fuel;
    }

    /**
     * Set active
     *
     * @param boolean $active
     *
     * @return Car
     */
    public function setActive($active)
    {
        $this->active = $active;

        return $this;
    }

    /**
     * Get active
     *
     * @return bool
     */
    public function getActive()
    {
        return $this->active;
    }

    /**
     * Set loadCapacity
     *
     * @param string $loadCapacity
     *
     * @return Car
     */
    public function setLoadCapacity($loadCapacity)
    {
        $this->loadCapacity = $loadCapacity;

        return $this;
    }

    /**
     * Get loadCapacity
     *
     * @return string
     */
    public function getLoadCapacity()
    {
        return $this->loadCapacity;
    }

    /**
     * Set numberOfSeats
     *
     * @param integer $numberOfSeats
     *
     * @return Car
     */
    public function setNumberOfSeats($numberOfSeats)
    {
        $this->numberOfSeats = $numberOfSeats;

        return $this;
    }

    /**
     * Get numberOfSeats
     *
     * @return int
     */
    public function getNumberOfSeats()
    {
        return $this->numberOfSeats;
    }
    /**
     * @var integer
     */
    private $master;


    /**
     * Set master
     *
     * @param integer $master
     *
     * @return Car
     */
    public function setMaster($master)
    {
        $this->master = $master;

        return $this;
    }

    /**
     * Get master
     *
     * @return integer
     */
    public function getMaster()
    {
        return $this->master;
    }
    /**
     * @var string
     */
    private $img;


    /**
     * Set img
     *
     * @param string $img
     *
     * @return Car
     */
    public function setImg($img)
    {
        $this->img = $img;

        return $this;
    }

    /**
     * Get img
     *
     * @return string
     */
    public function getImg()
    {
        return $this->img;
    }
}
